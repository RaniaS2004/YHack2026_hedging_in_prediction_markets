export function getSampleMarkets(platform?: Platform): NormalizedMarket[] {
  const now = new Date().toISOString();
  const withDefaults: NormalizedMarket[] = sampleMarkets.map((m) => ({
    ...m,
    yesBid: 0,
    yesAsk: 0,
    lastUpdated: now,
  }));
  if (platform) {
    return withDefaults.filter((m) => m.platform === platform);
  }
  return withDefaults;
}
